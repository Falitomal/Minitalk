#include "minitalk.h"

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + 48);
}

int	ft_atoi(const char *c)
{
	int	i;
	int	result;

	result = 0;
	i = 0;
	while (c[i] != '\0')
	{
		result *= 10;
		result += c[i] - 48;
		i++;
	}
	return (result);
}
